// Helper function to get English translation
export function getEnglishText(key: string): string {
  if (translations[key]) {
    return translations[key].en;
  }
  return key; // Return the key itself if no translation found
}

// Helper function to translate all Vietnamese text in a string
export function translateViToEn(text: string): string {
  let translatedText = text;

  // Replace all Vietnamese keys with their English translations
  Object.keys(translations).forEach(key => {
    translatedText = translatedText.replace(
      new RegExp(translations[key].vi, 'g'),
      translations[key].en
    );
  });

  return translatedText;
}
